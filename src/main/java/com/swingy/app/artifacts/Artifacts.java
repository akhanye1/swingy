package com.swingy.app.artifacts;

public class Artifacts {
	private String	name;
	private int		value;

	public Artifacts(String name, int Value) {
		this.name = name;
		this.value = value;
	}

	public String	getName() { return (this.name); }
	public int		getValue() { return (this.value); }
}
